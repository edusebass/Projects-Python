'''
Break:
Se utiliza para detener un loop inmediatamente en una condicion dada.
Es aconsejable dar una condicion "if" para utilizar el "break;", si no no funcionara.
'''
# UTILIZAMOS UN LOOP 
for i in range (2, 12):
  print(i)
# DAMOS UNA CONDICION PARA UTILIZAR EL BREAk
  if i == 7:
    print("Se detiene con la condicion en 7 por el if")
    break;
# COMO VEMOS SE DETIENE POR LA CONDICION CON BASE EN EL BREAK
